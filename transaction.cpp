#include "transaction.h"

namespace membles
{

uint64_t Transaction::count = 0;

/* ctor: Transaction
 * Set the transaction ID and increment the transaction count
 * The default priority level is always 0, the lowest level
 */
Transaction::Transaction(uint64_t addr,
                         uint32_t len,
                         bool is_read,
                         void *data)
    : id_(count++),
      addr_(addr),
      len_(len),
      is_read_(is_read),
      priority_(0),
      data_(data)
{}

}
